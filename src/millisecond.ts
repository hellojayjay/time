export const MILLISECOND_UNIT = 1

/** millisecond per second */
export const SECOND = 1000 * MILLISECOND_UNIT

/** millisecond per minute */
export const MINUTE = 60 * SECOND

/** millisecond per hour */
export const HOUR = 60 * MINUTE

/** millisecond per day */
export const DAY = 24 * HOUR

/** millisecond per week */
export const WEEK = 7 * DAY

/** millisecond per month with 28 days */
export const MONTH_28 = 28 * DAY

/** millisecond per month with 29 days */
export const MONTH_29 = 29 * DAY

/** millisecond per month with 30 days */
export const MONTH = 30 * DAY

/** millisecond per month with 31 days */
export const MONTH_31 = 31 * DAY

/** millisecond per year with 365 days */
export const YEAR = 365 * DAY

/** millisecond per year with 366 days */
export const YEAR_366 = 366 * DAY
